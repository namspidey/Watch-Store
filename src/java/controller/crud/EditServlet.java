    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String cidraw = request.getParameter("cid");
        CategoryDAO cd= new CategoryDAO();
        int cid = Integer.parseInt(cidraw);
        Category cate = cd.getCategoryById(cid);
        request.setAttribute("cate", cate);
        request.getRequestDispatcher("updateCate.jsp").forward(request, response);
        
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String cname=request.getParameter("cname");
        String idr = request.getParameter("id");
        int id = Integer.parseInt(idr);
        CategoryDAO cd= new CategoryDAO();
        Category c =cd.getCategoryById(id);
        cd.updateCategory(cname, c.getCid());
        
        response.sendRedirect("category");
    }

